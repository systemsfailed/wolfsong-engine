package inactive;

public class SpriteRule 
{
	private int 
	rows, cols, height, width, frameSpeed;
	
	public SpriteRule(int rows, int cols, int height, int width, int frameSpeed)
	{
		this.rows = rows;
		this.cols = cols;
		this.height = height;
		this.width = width;
		this.frameSpeed = frameSpeed;
	}

	public int getRows() {
		return rows;
	}

	public int getCols() {
		return cols;
	}

	public int getHeight() {
		return height;
	}

	public int getWidth() {
		return width;
	}
	
	
	
	
}
